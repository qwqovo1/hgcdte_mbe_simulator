# 便捷函数：根据模式获取对应的 System Prompt
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def get_system_prompt(mode: str = "chat", enhanced: bool = False) -> str:
    """
    根据分析模式返回对应的 System Prompt

    参数:
        mode: 分析模式
            - "chat"      : 通用对话分析
            - "recommend"  : 工艺参数推荐
            - "diagnose"   : 生长诊断
            - "report"     : 报告生成
        enhanced: 是否使用增强版（含课题组DFT数据）

    返回:
        完整的 System Prompt 字符串
    """
    # 选择基础 prompt
    base = SYSTEM_PROMPT_ENHANCED if enhanced else SYSTEM_PROMPT

    # 根据模式追加专用指令
    mode_prompts = {
        "chat": "",
        "recommend": "\n\n" + RECOMMEND_PROMPT,
        "diagnose": "\n\n" + DIAGNOSE_PROMPT,
        "report": "\n\n" + REPORT_PROMPT,
    }

    suffix = mode_prompts.get(mode, "")
    return base + suffix


